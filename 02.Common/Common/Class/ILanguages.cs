public class ILanguages
{
    private int _STT;
    private string _MS_MODULE;
    private string _FORM;
    private string _KEYWORD;
    private string _VIETNAM;
    private string _ENGLISH;
    public int STT
    {
        get
        {
            return _STT;
        }
        set
        {
            _STT = value;
        }
    }
    public string MS_MODULE
    {
        get
        {
            return _MS_MODULE;
        }
        set
        {
            _MS_MODULE = value;
        }
    }
    public string FORM
    {
        get
        {
            return _FORM;
        }
        set
        {
            _FORM = value;
        }
    }
    public string KEYWORD
    {
        get
        {
            return _KEYWORD;
        }
        set
        {
            _KEYWORD = value;
        }
    }
    public string VIETNAM
    {
        get
        {
            return _VIETNAM;
        }
        set
        {
            _VIETNAM = value;
        }
    }
    public string ENGLISH
    {
        get
        {
            return _ENGLISH;
        }
        set
        {
            _ENGLISH = value;
        }
    }
}
